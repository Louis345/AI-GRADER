/**
 * Generates HTML output for the evaluation results
 * @param {string} studentName - Student's name
 * @param {string} studentEmail - Student's email (optional)
 * @param {number} weekNumber - Week number
 * @param {string} assignmentName - Assignment name
 * @param {Object} aiResponse - AI evaluation response
 * @returns {string} HTML document
 */
function generateHtml(
  studentName,
  studentEmail,
  weekNumber,
  assignmentName,
  aiResponse
) {
  return `
<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Grading: ${studentName} - ${assignmentName}</title>
  <!-- Student data for reporting -->
  <meta name="student-name" content="${studentName}">
  <meta name="student-email" content="${studentEmail || "N/A"}">
  <meta name="week-number" content="${weekNumber}">
  <meta name="assignment-name" content="${assignmentName}">
  <meta name="grading-date" content="${new Date().toISOString()}">
  <style>
    body { 
      font-family: Arial, sans-serif; 
      line-height: 1.6;
      max-width: 800px;
      margin: 0 auto;
      padding: 20px;
    }
    .header { 
      text-align: center;
      margin-bottom: 30px;
      border-bottom: 2px solid #0275d8;
      padding-bottom: 15px;
    }
    h1, h2, h3, h4 {
      color: #0275d8;
    }
    .student-info {
      background-color: #f0f8ff;
      padding: 10px;
      border-radius: 5px;
      margin-bottom: 20px;
      border-left: 4px solid #0275d8;
    }
    .section {
      margin-bottom: 20px;
      padding: 15px;
      border-radius: 5px;
      background-color: #f8f9fa;
    }
    .score {
      font-weight: bold;
      color: #0056b3;
    }
    .total-score {
      font-size: 18px;
      text-align: center;
      margin: 20px 0;
      padding: 10px;
      background-color: #e9ecef;
      border-radius: 5px;
    }
    .emoji {
      font-size: 1.2em;
    }
    ul {
      padding-left: 20px;
    }
    li {
      margin-bottom: 8px;
    }
    /* Copy button styling */
    .copy-button {
      display: block;
      margin: 20px auto;
      padding: 10px 20px;
      background-color: #0275d8;
      color: white;
      border: none;
      border-radius: 5px;
      cursor: pointer;
      font-size: 16px;
    }
    .copy-button:hover {
      background-color: #0056b3;
    }
    .footer {
      text-align: center;
      margin-top: 30px;
      font-size: 0.8em;
      color: #6c757d;
    }
  </style>
</head>
<body>
  <div class="header">
    <h1>${assignmentName} Evaluation</h1>
    <h2>Student: ${studentName}</h2>
    <p>Week ${weekNumber}</p>
  </div>
  
  <div class="student-info">
    <p><strong>Student Email:</strong> ${studentEmail || "N/A"}</p>
  </div>
  
  <div id="content-to-copy">
    ${aiResponse.formattedHtml}
  </div>
  
  <button class="copy-button" onclick="copyContent()">Copy Feedback for LMS</button>
  
  <div class="footer">
    <p>Generated by AI Grader on ${new Date().toLocaleString()}</p>
  </div>
  
  <script>
    function copyContent() {
      const content = document.getElementById('content-to-copy').innerText;
      navigator.clipboard.writeText(content)
        .then(() => alert('Feedback copied to clipboard!'))
        .catch(err => console.error('Failed to copy: ', err));
    }
  </script>
</body>
</html>
  `;
}

module.exports = { generateHtml };
